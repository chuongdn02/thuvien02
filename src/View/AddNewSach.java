    private void themSachButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themSachButtonActionPerformed
        if(maTLSachTF.getText().equals("") ||
            tenNSBSachTF.getText().equals("") ||
            soBanPhatHanhSachTF.getText().equals("") ||
            tenTGSachTF.getText().equals("") ||
            soTrangSachTF.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "hãy nhập đầy đủ!",
                "Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }

        for(Sach sach : handle.GetDataSach()) {
            if(maTLSachTF.getText().equals(sach.getMaTaiLieu())) {
                JOptionPane.showMessageDialog(rootPane, "tồn tại mã này trong ds!! ",
                    "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        Sach sach = new Sach();
        sach.setMaTaiLieu(maTLSachTF.getText());
        sach.setSoBanPhatHanh(Integer.valueOf(soBanPhatHanhSachTF.getText()));
        sach.setTenNSB(tenNSBSachTF.getText());
        sach.setTenTG(tenTGSachTF.getText());
        sach.setSoTrang(Integer.valueOf(soTrangSachTF.getText()));

        handle.AddNewBook(sach);

        JOptionPane.showMessageDialog(rootPane, "thành công!");
        this.dispose();
        new ThuVien().setVisible(true);
    }//GEN-LAST:event_themSachButtonActionPerformed

